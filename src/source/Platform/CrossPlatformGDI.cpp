BOOL GetTextExtentPoint32(HDC hdc, const wchar_t* lpString, int cchString, SIZE* lpSize)
{
    if (!lpSize)
    {
        return FALSE;
    }

    if (cchString <= 0 || !lpString)
    {
        lpSize->cx = 0;
        lpSize->cy = MU_FONT_CHAR_HEIGHT;
        return TRUE;
    }

    // Use the font's requested height if a font is selected, otherwise use base size
    int charHeight = MU_FONT_CHAR_HEIGHT;
    if (hdc)
    {
        auto* dc = static_cast<MuGdiDC*>(hdc);
        if (dc->type == MU_GDI_OBJ_DC && dc->pFont)
        {
            charHeight = dc->pFont->nHeight;
        }
    }

    // Scale character width proportionally to height
    int charWidth = (MU_FONT_CHAR_WIDTH * charHeight + MU_FONT_CHAR_HEIGHT / 2) / MU_FONT_CHAR_HEIGHT;
    if (charWidth < 1)
    {
        charWidth = 1;
    }

    lpSize->cx = static_cast<LONG>(cchString) * charWidth;
    lpSize->cy = charHeight;
    return TRUE;
}

BOOL TextOut(HDC hdc, int x, int y, const wchar_t* lpString, int cchString)
{
    if (!hdc || !lpString || cchString <= 0)
    {
        return FALSE;
    }

    auto* dc = static_cast<MuGdiDC*>(hdc);
    if (dc->type != MU_GDI_OBJ_DC || !dc->pBitmap || !dc->pBitmap->pBits)
    {
        return FALSE;
    }

    unsigned char* buffer = dc->pBitmap->pBits;
    const int bufW = dc->pBitmap->width;
    const int bufH = dc->pBitmap->height;
    const int bpp = dc->pBitmap->bytesPerPixel;

    // Determine rendering scale from font height
    int scaleY = MU_FONT_CHAR_HEIGHT;
    int scaleX = MU_FONT_CHAR_WIDTH;
    if (dc->pFont && dc->pFont->nHeight > 0)
    {
        scaleY = dc->pFont->nHeight;
        scaleX = (MU_FONT_CHAR_WIDTH * scaleY + MU_FONT_CHAR_HEIGHT / 2) / MU_FONT_CHAR_HEIGHT;
        if (scaleX < 1)
        {
            scaleX = 1;
        }
    }

    for (int i = 0; i < cchString; ++i)
    {
        wchar_t ch = lpString[i];

        // Map to font table index; fallback to '?' for unsupported characters
        int fontIdx;
        if (ch >= MU_FONT_FIRST_CHAR && ch <= MU_FONT_LAST_CHAR)
        {
            fontIdx = ch - MU_FONT_FIRST_CHAR;
        }
        else
        {
            fontIdx = '?' - MU_FONT_FIRST_CHAR;
        }

        const unsigned char* glyph = &g_muBitmapFont8x16[fontIdx * MU_FONT_CHAR_HEIGHT];

        // Render glyph with nearest-neighbor scaling
        for (int row = 0; row < scaleY; ++row)
        {
            int srcRow = (row * MU_FONT_CHAR_HEIGHT) / scaleY;
            if (srcRow >= MU_FONT_CHAR_HEIGHT)
            {
                srcRow = MU_FONT_CHAR_HEIGHT - 1;
            }
            unsigned char bits = glyph[srcRow];

            for (int col = 0; col < scaleX; ++col)
            {
                int srcCol = (col * MU_FONT_CHAR_WIDTH) / scaleX;
                if (srcCol >= MU_FONT_CHAR_WIDTH)
                {
                    srcCol = MU_FONT_CHAR_WIDTH - 1;
                }

                if (bits & (0x80 >> srcCol))
                {
                    int px = x + i * scaleX + col;
                    int py = y + row;

                    if (px >= 0 && px < bufW && py >= 0 && py < bufH)
                    {
                        int offset = (py * bufW + px) * bpp;
                        // Write white pixels — WriteText() handles color replacement
                        for (int b = 0; b < bpp; ++b)
                        {
                            buffer[offset + b] = 255;
                        }
                    }
                }
            }
        }
    }

    return TRUE;
}

