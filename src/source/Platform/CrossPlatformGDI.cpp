// Thread-local storage for the current clipboard text (freed on CloseClipboard)
static thread_local char* s_clipboardText = nullptr;

BOOL OpenClipboard(HWND /*hwnd*/)
{
    // SDL3 doesn't require explicit open. Always succeeds.
    return TRUE;
}

HGLOBAL GetClipboardData(UINT /*uFormat*/)
{
    // Free any previous clipboard text
    if (s_clipboardText)
    {
        SDL_free(s_clipboardText);
        s_clipboardText = nullptr;
    }

    s_clipboardText = SDL_GetClipboardText();
    if (!s_clipboardText || s_clipboardText[0] == '\0')
    {
        if (s_clipboardText)
        {
            SDL_free(s_clipboardText);
            s_clipboardText = nullptr;
        }
        return nullptr;
    }

    return static_cast<HGLOBAL>(s_clipboardText);
}

void* GlobalLock(HGLOBAL hMem)
{
    // SDL clipboard text is already a plain pointer — just return it
    return hMem;
}

BOOL GlobalUnlock(HGLOBAL /*hMem*/)
{
    // No-op — cleanup happens in CloseClipboard
    return TRUE;
}

BOOL CloseClipboard()
{
    if (s_clipboardText)
    {
        SDL_free(s_clipboardText);
        s_clipboardText = nullptr;
    }
    return TRUE;
}

